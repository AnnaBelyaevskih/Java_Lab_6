package task6;

/**
 * Пример класса, который помечен аннотацией @Cache.
 * В аннотации указываются области, которые нужно кешировать: в этом примере
 * "users" и "products".
 *
 * Метод printCache показывает, как получить список областей из аннотации
 * и вывести их на экран. Если список пустой, выводится сообщение,
 * что кешируемых областей нет.
 */
@Cache({"users", "products"})
public class CacheExample {

    /**
     * Метод выводит список кешируемых областей для этого класса.
     *
     * Сначала метод получает аннотацию с класса. Потом проверяет, есть ли
     * области в массиве value. Если массив пустой, выводится сообщение,
     * что кешируемые области отсутствуют. Иначе перечисляются все области.
     */
    public static void printCache() {
        // Получаем аннотацию с этого класса
        Cache cache = CacheExample.class.getAnnotation(Cache.class);

        // Проверяем, есть ли области
        if (cache.value().length == 0) {
            System.out.println("Кешируемые области отсутствуют.");
        } else {
            System.out.println("Кешируемые области:");
            for (String area : cache.value()) {
                System.out.println("- " + area);
            }
        }
    }
}
