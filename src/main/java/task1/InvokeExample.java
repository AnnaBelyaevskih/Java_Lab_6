package task1;

/**
 * Пример класса, в котором есть метод с аннотацией @Invoke.
 * Метод с этой аннотацией можно вызвать автоматически через специальный
 * обработчик. Другие методы остаются без изменений и не вызываются.
 */
public class InvokeExample {

    /**
     * Флаг, который показывает, был ли вызван метод execute.
     * Если метод выполнен, флаг становится true.
     */
    private boolean executed;

    /**
     * Метод с аннотацией @Invoke.
     * Когда этот метод вызывается, он меняет значение флага executed на true
     * и выводит сообщение в консоль.
     */
    @Invoke
    public void execute() {
        executed = true;
        System.out.println("Метод с аннотацией @Invoke выполнен.");
    }

    /**
     * Метод показывает, был ли выполнен метод execute.
     * @return true, если метод execute был вызван, иначе false
     */
    public boolean isExecuted() {
        return executed;
    }

    /**
     * Пример метода без аннотации.
     * Этот метод не будет вызван автоматически. Его можно вызвать только вручную.
     */
    public void anotherMethod() {
        System.out.println("Этот метод не будет вызван автоматически.");
    }
}
